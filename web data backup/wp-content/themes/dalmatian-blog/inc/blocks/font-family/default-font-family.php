<?php

/* Default Font Family */
function dalmatian_blog_get_default_site_identity_font_family() {    
    return esc_html__( "Inika", 'dalmatian-blog' );
}

function dalmatian_blog_get_default_main_font_family() {    
    return esc_html__( "Lato", 'dalmatian-blog' );
}

function dalmatian_blog_get_default_secondary_font_family() {    
    return esc_html__( "Noto Serif", 'dalmatian-blog' );
}